"""
Q3. Ask 3 numbers from User and Calculate the Average.
"""

first_number: int = int(input("Enter the 1st number : "))
second_number: int = int(input("Enter the 2nd number : "))
third_number: int = int(input("Enter the 3rd number : "))

average = first_number + second_number + third_number
print(f"Average of three numbers is : {average}")
